    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        
            String sid = txtno.getText();
            insert = connection.prepareStatement("select * from student where id::varchar = ?");
            insert.setString(1, sid);
            rs = insert.executeQuery();
            
            
            if(rs.next()==false){
                JOptionPane.showMessageDialog(this, "Student No Not Found");
                txtstname.setText("");
            }else{
                String name = rs.getString("stname");
                txtstname.setText(name.trim());
                
                String classes = rs.getString("class");
                txtclass.addItem(classes.trim());
                
            }
        
        
        
         Marks_load();
        
        }catch (SQLException ex) {
            Logger.getLogger(marks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String stno = txtno.getText();
        String classes = txtclass.getSelectedItem().toString();
        String subject = txtsubject.getSelectedItem().toString();
        String marks = txtmarks.getText();
        String term = txtterm.getSelectedItem().toString();


        
        
        try{
            insert = connection.prepareStatement("insert into marks(stid,class,subject,marks,term) values (?,?,?,?,?) ");
            insert.setString(1, stno);
            insert.setString(2, classes);
            insert.setString(3, subject);
            insert.setString(4, marks);
            insert.setString(5, term);
            
                
            insert.executeUpdate();
            JOptionPane.showMessageDialog(this,"Marks Added");
            Marks_load();
            
           
        } catch (SQLException ex){
            Logger.getLogger(student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try { 
          
        d = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        String id = d.getValueAt(selectedIndex, 0).toString();
       
            insert = connection.prepareStatement("delete from marks where id::varchar = ? ");
            insert.setString(1, id);       
            insert.executeUpdate();
            JOptionPane.showMessageDialog(this,"Student Deleted");
            jButton1.setEnabled(true);

            txtno.setText("");
            txtclass.setSelectedItem(-1);
            txtsubject.setSelectedItem(-1);
            txtmarks.setText("");
            txtterm.setSelectedItem(-1);
            
            Marks_load();
           
        } catch (SQLException ex) {
            Logger.getLogger(user.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

