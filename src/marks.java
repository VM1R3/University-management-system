    public void Load_Subject(){
        try {
        insert = connection.prepareStatement("select Distinct subjectname from subject");
        rs=insert.executeQuery();
        txtsubject.removeAllItems();

        while(rs.next()){
            txtsubject.addItem(rs.getString("subjectname"));
        }

        }catch (SQLException ex) {
            Logger.getLogger(Exam.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    public void Marks_load() {
        int c;
        try {
            insert = connection.prepareStatement("select * from marks");
            rs = insert.executeQuery();
            
         
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                Vector v2 = new Vector();
                
                for(int a = 1; a <= c; a++) {
                    v2.add(rs.getString("id"));
                    
                    v2.add(rs.getString("class"));
                    v2.add(rs.getString("subject"));
                    v2.add(rs.getString("marks"));
                    v2.add(rs.getString("term"));            
                     
                    
                    
                }

                d.addRow(v2);

            }
            
        } catch (SQLException ex) {
            Logger.getLogger(user.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    
    
    
    
    
    
    

